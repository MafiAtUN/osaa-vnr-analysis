/* Regenerates assets/js/manifest.js and assets/js/manifest_slugs.json from
   whatever country files actually exist in assets/js/data.

   The site therefore can never link to a country whose data is missing —
   which is how the old build ended up advertising "2 Southern African
   countries" while rendering none of them.

   Run: node build-manifest.js   */
const fs = require("fs");
const path = require("path");

const dir = path.join(__dirname, "assets/js/data");
const slugs = fs.readdirSync(dir)
  .filter(f => f.endsWith(".js"))
  .map(f => f.replace(/\.js$/, ""))
  .sort();

fs.writeFileSync(
  path.join(__dirname, "assets/js/manifest_slugs.json"),
  JSON.stringify(slugs, null, 2) + "\n"
);

fs.writeFileSync(path.join(__dirname, "assets/js/manifest.js"),
`/* GENERATED by build-manifest.js — do not edit by hand.
   Countries currently published: ${slugs.length}. */
window.VNR_COUNTRIES = ${JSON.stringify(slugs, null, 2)};

/* Loaded synchronously and in order, before the page controller runs.
   document.write is deliberate: it preserves execution order without a
   bundler and behaves identically on file:// and GitHub Pages. */
(function () {
  var base = (document.currentScript && document.currentScript.src || "")
    .replace(/manifest\\.js.*$/, "");
  window.VNR_COUNTRIES.forEach(function (slug) {
    document.write('<script src="' + base + 'data/' + slug + '.js"><\\/script>');
  });
})();
`);

console.log(`manifest: ${slugs.length} countries — ${slugs.join(", ")}`);
